    private void buscarCursos (){
        try{
            DAO dao = new DAO();
            Curso[] cursos = dao.obterCursos();
            cursosComboBox.setModel(new DefaultComboBoxModel<>(cursos));
        }
        catch (Exception e){
            JOptionPane.showMessageDialog(null, "Cursos indispon√≠veis, tente novamente mais tarde.");
            e.printStackTrace();
        }
    }    
    
    
